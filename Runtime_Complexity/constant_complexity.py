# Constant Runtime Complexity: O(1)

# Constant runtime complexity is the best case scenario for an algorithm. It means that the runtime of an algorithm is the same regardless of the size of the input data set.

def sum_of_two_numbers(num1, num2):

    """
    This function returns the sum of two numbers.
    """
    return num1 + num2

if __name__ == '__main__':
    print(sum_of_two_numbers(10, 20))

# Output: 30